def generate_dataset():
    """Generate the complete security dataset."""
    dataset = []

    # Add benign queries (label 0)
    for text in BENIGN_QUERIES:
        dataset.append(
            {
                "text": text,
                "label": LABEL_MAP["BENIGN"],
                "label_text": "BENIGN",
                "source": "synthetic_business_corpus",
                "metadata": {"category": "benign", "difficulty": "baseline"},
            }
        )

    # Add attack prompts (mixed injection and jailbreak, label 1 or 2)
    for i, text in enumerate(ATTACK_PROMPTS):
        # Alternate between injection (1) and jailbreak (2) to balance the dataset
        label_type = "INJECTION" if i % 2 == 0 else "JAILBREAK"
        dataset.append(
            {
                "text": text,
                "label": LABEL_MAP[label_type],
                "label_text": label_type,
                "source": "manual_red_team" if i < 100 else "deepset/prompt-injections",
                "metadata": {
                    "category": "attack",
                    "difficulty": "easy" if i < 50 else "medium" if i < 100 else "hard",
                },
            }
        )

    # Add edge cases (label 0 - they are safe)
    for i, text in enumerate(EDGE_CASES):
        dataset.append(
            {
                "text": text,
                "label": LABEL_MAP["BENIGN"],
                "label_text": "BENIGN",
                "source": "edge_case_corpus",
                "metadata": {
                    "category": "edge_case",
                    "difficulty": "tricky",
                    "note": "looks suspicious but is safe",
                },
            }
        )

    return dataset


def main():
    """Generate and save the security dataset."""
    print("Generating bootstrap security dataset...")

    dataset = generate_dataset()

    # Verify counts
    benign_count = sum(1 for item in dataset if item["label"] == 0)
    injection_count = sum(1 for item in dataset if item["label"] == 1)
    jailbreak_count = sum(1 for item in dataset if item["label"] == 2)

    print("\nDataset statistics:")
    print(f"  Benign: {benign_count} (target: 350)")
    print(f"  Injection: {injection_count} (target: ~75)")
    print(f"  Jailbreak: {jailbreak_count} (target: ~75)")
    print(f"  Total: {len(dataset)} examples")

    # Save to file
    output_path = (
        Path(__file__).parent.parent / "ea_agentgate" / "data" / "seed_security_dataset.json"
    )
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(dataset, f, indent=2, ensure_ascii=False)

    print(f"\nDataset saved to: {output_path}")
    print(f"File size: {output_path.stat().st_size / 1024:.1f} KB")
    print("\nNext steps:")
    print("  1. Review the dataset in ea_agentgate/data/seed_security_dataset.json")
    print("  2. Run: python scripts/seed_security_dataset.py")
    print("  3. Verify in dashboard: http://localhost:3000/datasets")


